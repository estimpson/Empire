create procedure [EEIUser].[usp_QT_GetApplicationCodes]
as
declare
	@TranDT datetime = null
,	@Result integer = null

set nocount on
set ansi_warnings off
set	@Result = 999999

--- <Error Handling>
declare
	@CallProcName sysname,
	@TableName sysname,
	@ProcName sysname,
	@ProcReturn integer,
	@ProcResult integer,
	@Error integer,
	@RowCount integer

set	@ProcName = user_name(objectproperty(@@procid, 'OwnerId')) + '.' + object_name(@@procid)  -- e.g. EEIUser.usp_Test
--- </Error Handling>

--- <Tran Required=Yes AutoCreate=Yes TranDTParm=Yes>
declare
	@TranCount smallint

set	@TranCount = @@TranCount
if	@TranCount = 0 begin
	begin tran @ProcName
end
else begin
	save tran @ProcName
end
set	@TranDT = coalesce(@TranDT, GetDate())
--- </Tran>

---	<ArgumentValidation>

---	</ArgumentValidation>

--- <Body>
declare @QT_ApplicationCodes table
(	ApplicationID int not null IDENTITY(1, 1) primary key
,	ApplicationCode varchar(10)
)

insert
	@QT_ApplicationCodes
select
	qql.ApplicationCode
from
	EEIUser.QT_QuoteLog qql
group by
	qql.ApplicationCode  
--- </Body>

---	<CloseTran AutoCommit=Yes>
if	@TranCount = 0 begin
	commit tran @ProcName
end
---	</CloseTran AutoCommit=Yes>

---	<Return>
select
	*
from
	@QT_ApplicationCodes
set	@Result = 0
return
	@Result
--- </Return>

